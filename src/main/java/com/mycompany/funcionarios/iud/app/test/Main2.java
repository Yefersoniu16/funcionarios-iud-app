                txtFuncionarioId.setText(String.valueOf(selectedFuncionario.getId()));
                txtEdiTDoc.setText(selectedFuncionario.getTipo_doccumento());
                txtEdiNDoc.setText(selectedFuncionario.getNumero_documento());
